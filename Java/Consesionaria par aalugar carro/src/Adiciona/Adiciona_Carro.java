    private void BTN_AddCarroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_AddCarroActionPerformed
        Carro novo_carro = new Carro(Integer.parseInt(ChassiAdd.getText()),
                ModeloAdd.getText(),
                Integer.parseInt(AnoAdd.getText()),
                PlacaAdd.getText(),
                Float.parseFloat(KmAdd.getText()));

        if (rep.addCarro(novo_carro)) {
            JOptionPane.showMessageDialog(rootPane, "Cadastro concluido!");
            try {
                rep.escreverArquivo("Carro.csv");
            } catch (Exception ex) {
                Logger.getLogger(Adiciona_Carro.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Cadastro n√£o concluido");
        }

        this.dispose();
        new FrameMenu(rep).setVisible(true);

    }//GEN-LAST:event_BTN_AddCarroActionPerformed


