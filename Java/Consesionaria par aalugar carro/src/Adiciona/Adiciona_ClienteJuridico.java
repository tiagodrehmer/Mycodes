    private void BTN_concluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_concluirActionPerformed
        ClienteJuridico novo_cliente = new ClienteJuridico(NomeAdd.getText(),
                EndAdd.getText(),
                FoneAdd.getText(),
                CnpjAdd.getText().trim(),
                ProAdd.getText());
        
        if(rep.addClienteJuridico(novo_cliente)){
            JOptionPane.showMessageDialog(rootPane, "Cadastro concluido!");
            this.dispose();
            this.dispose();
            new Adiciona_Contrato(novo_cliente, rep).setVisible(true);
        }else{
            JOptionPane.showMessageDialog(rootPane, "Cnpj ja cadastrado");
        }
    }//GEN-LAST:event_BTN_concluirActionPerformed


